package com.mwsx.model;

import java.util.List;

public class NewMappingFileInfo {

	String id;
	String version;
	String description;
	String databaseConnectionName;
	List<String> mappingTemplates;
	List<Prefix> prefixes;
	DatabaseConnection newDatabaseConnection;
	String newDatabaseConnectionDescription;
	
	public String getNewDatabaseConnectionDescription() {
		return newDatabaseConnectionDescription;
	}
	public void setNewDatabaseConnectionDescription(String newDatabaseConnectionDescription) {
		this.newDatabaseConnectionDescription = newDatabaseConnectionDescription;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getDatabaseConnectionName() {
		return databaseConnectionName;
	}
	public void setDatabaseConnectionName(String databaseConnectionName) {
		this.databaseConnectionName = databaseConnectionName;
	}
	public DatabaseConnection getNewDatabaseConnection() {
		return newDatabaseConnection;
	}
	public void setNewDatabaseConnection(DatabaseConnection newDatabaseConnection) {
		this.newDatabaseConnection = newDatabaseConnection;
	}
	public List<String> getMappingTemplates() {
		return mappingTemplates;
	}
	public void setMappingTemplates(List<String> mappingTemplates) {
		this.mappingTemplates = mappingTemplates;
	}
	public List<Prefix> getPrefixes() {
		return prefixes;
	}
	public void setPrefixes(List<Prefix> prefixes) {
		this.prefixes = prefixes;
	}
	
}
