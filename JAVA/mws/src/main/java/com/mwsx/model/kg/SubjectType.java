package com.mwsx.model.kg;

public class SubjectType {
	String subject_type;
    String subject_type_short;
    int instance_count;
    int page_count;
	public String getSubject_type() {
		return subject_type;
	}
	public void setSubject_type(String subject_type) {
		this.subject_type = subject_type;
	}
	public String getSubject_type_short() {
		return subject_type_short;
	}
	public void setSubject_type_short(String subject_type_short) {
		this.subject_type_short = subject_type_short;
	}
	public int getInstance_count() {
		return instance_count;
	}
	public void setInstance_count(int instance_count) {
		this.instance_count = instance_count;
	}
	public int getPage_count() {
		return page_count;
	}
	public void setPage_count(int page_count) {
		this.page_count = page_count;
	}
    
    
}
