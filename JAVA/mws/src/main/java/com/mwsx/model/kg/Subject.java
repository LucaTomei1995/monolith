package com.mwsx.model.kg;

public class Subject {
	String subject_resource;
	String subject_label;
	String subject_resource_short;
	public String getSubject_resource() {
		return subject_resource;
	}
	public void setSubject_resource(String subject_resource) {
		this.subject_resource = subject_resource;
	}
	public String getSubject_label() {
		return subject_label;
	}
	public void setSubject_label(String subject_label) {
		this.subject_label = subject_label;
	}
	public String getSubject_resource_short() {
		return subject_resource_short;
	}
	public void setSubject_resource_short(String subject_resource_short) {
		this.subject_resource_short = subject_resource_short;
	}
	
}
